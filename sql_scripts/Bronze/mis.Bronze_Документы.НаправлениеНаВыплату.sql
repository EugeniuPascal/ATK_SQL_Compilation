USE [ATK];
GO

IF OBJECT_ID('mis.[Bronze_Документы.НаправлениеНаВыплату]', 'U') IS NOT NULL
    DROP TABLE mis.[Bronze_Документы.НаправлениеНаВыплату];
GO

CREATE TABLE mis.[Bronze_Документы.НаправлениеНаВыплату]
(
    [НаправлениеНаВыплату Дата] DATETIME NULL,
    [НаправлениеНаВыплату Кредит ID] VARCHAR(36) NOT NULL,
    [НаправлениеНаВыплату ID] VARCHAR(36) NOT NULL,
    [НаправлениеНаВыплату Форма Кредитования ID] VARCHAR(36) NOT NULL,
    [НаправлениеНаВыплату Пометка Удаления] VARCHAR(36) NOT NULL,
    [НаправлениеНаВыплату Проведен] VARCHAR(36) NOT NULL,
    [НаправлениеНаВыплату Источник Заполнения] NVARCHAR(256) NULL,
    [НаправлениеНаВыплату Категория Риска AML] NVARCHAR(256) NULL
);
GO

/* ======================================================
   1. Last valid document per CreditID
====================================================== */
WITH LastValid AS (
    SELECT *,
           ROW_NUMBER() OVER (
               PARTITION BY [НаправлениеНаВыплату Кредит ID]
               ORDER BY [НаправлениеНаВыплату Дата] DESC
           ) AS rn
    FROM [ATK].[dbo].[Документы.НаправлениеНаВыплату]
    WHERE [НаправлениеНаВыплату Пометка Удаления] = '00'
      AND [НаправлениеНаВыплату Проведен] = '00'
),

/* ======================================================
   2. FIRST non-empty Source by date (special form only)
====================================================== */
CreditLine AS (
    SELECT
        CreditID,
        [НаправлениеНаВыплату Источник Заполнения]
    FROM (
        SELECT
            [НаправлениеНаВыплату Кредит ID] AS CreditID,
            [НаправлениеНаВыплату Источник Заполнения],
            ROW_NUMBER() OVER (
                PARTITION BY [НаправлениеНаВыплату Кредит ID]
                ORDER BY [НаправлениеНаВыплату Дата] ASC
            ) AS rn
        FROM [ATK].[dbo].[Документы.НаправлениеНаВыплату]
        WHERE [НаправлениеНаВыплату Форма Кредитования ID]
              = 'BB410A1C78CE024C43C0650E736EEB95'
          AND NULLIF(LTRIM(RTRIM([НаправлениеНаВыплату Источник Заполнения])), '') IS NOT NULL
    ) s
    WHERE rn = 1
)

/* ======================================================
   3. Insert into Bronze
====================================================== */
INSERT INTO mis.[Bronze_Документы.НаправлениеНаВыплату]
(
    [НаправлениеНаВыплату Дата],
    [НаправлениеНаВыплату Кредит ID],
    [НаправлениеНаВыплату ID],
    [НаправлениеНаВыплату Форма Кредитования ID],
    [НаправлениеНаВыплату Пометка Удаления],
    [НаправлениеНаВыплату Проведен],
    [НаправлениеНаВыплату Источник Заполнения],
    [НаправлениеНаВыплату Категория Риска AML]
)
SELECT
    lv.[НаправлениеНаВыплату Дата],
    lv.[НаправлениеНаВыплату Кредит ID],
    lv.[НаправлениеНаВыплату ID],
    lv.[НаправлениеНаВыплату Форма Кредитования ID],
    lv.[НаправлениеНаВыплату Пометка Удаления],
    lv.[НаправлениеНаВыплату Проведен],

    CASE
        WHEN lv.[НаправлениеНаВыплату Форма Кредитования ID]
             = 'BB410A1C78CE024C43C0650E736EEB95'
        THEN cl.[НаправлениеНаВыплату Источник Заполнения]
        ELSE lv.[НаправлениеНаВыплату Источник Заполнения]
    END AS [НаправлениеНаВыплату Источник Заполнения],

    lv.[НаправлениеНаВыплату Категория Риска AML]
FROM LastValid lv
LEFT JOIN CreditLine cl
    ON cl.CreditID = lv.[НаправлениеНаВыплату Кредит ID]
WHERE lv.rn = 1;
GO
