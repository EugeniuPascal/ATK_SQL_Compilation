          AND tgt.[СуммыЗадолженностиПоПериодамПросрочки Кредит ID] = src.[СуммыЗадолженностиПоПериодамПросрочки Кредит ID]
          AND tgt.[СуммыЗадолженностиПоПериодамПросрочки Клиент ID] = src.[СуммыЗадолженностиПоПериодамПросрочки Клиент ID]
