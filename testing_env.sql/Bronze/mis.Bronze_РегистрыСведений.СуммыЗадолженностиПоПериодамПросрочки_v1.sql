(
    [СуммыЗадолженностиПоПериодамПросрочки Дата],
    [СуммыЗадолженностиПоПериодамПросрочки Кредит ID],
    [СуммыЗадолженностиПоПериодамПросрочки Клиент ID],
    [СуммыЗадолженностиПоПериодамПросрочки Количество Дней Просрочки Кредит],
    [СуммыЗадолженностиПоПериодамПросрочки Фактическое Количество Дней Просрочки Итого],
