USE [ATK];
GO

-- ===============================================
-- Drop the target table if it already exists
-- ===============================================
IF OBJECT_ID('mis.[Bronze_РегистрыСведений.ДанныеКредитовВыданных_CreditAmtInMDL]', 'U') IS NOT NULL
    DROP TABLE mis.[Bronze_РегистрыСведений.ДанныеКредитовВыданных_CreditAmtInMDL];
GO

-- ===============================================
-- Create the table to store credit amounts in MDL
-- ===============================================
CREATE TABLE mis.[Bronze_РегистрыСведений.ДанныеКредитовВыданных_CreditAmtInMDL]
(
    [ДанныеКредитовВыданных Период] DATETIME NULL,  
    [ДанныеКредитовВыданных Кредит ID]  VARCHAR(36) NOT NULL, 
    [ДанныеКредитовВыданных Валюта Кредита ID] VARCHAR(36) NOT NULL, 
    [ДанныеКредитовВыданных Валюта Кредита] NVARCHAR(50) NULL, 
    [ДанныеКредитовВыданных Дата Выдачи] DATETIME NULL, 
    [ДанныеКредитовВыданных Сумма Кредита] DECIMAL(15, 2) NULL,
    [ДанныеКредитовВыданных Кредитный Продукт] NVARCHAR(100) NULL,
    CreditAmountInMDL DECIMAL(18,2) NULL    

);
GO

-- ===============================================
-- Insert data into the new table
-- ===============================================
INSERT INTO mis.[Bronze_РегистрыСведений.ДанныеКредитовВыданных_CreditAmtInMDL] 
(
    [ДанныеКредитовВыданных Период],
    [ДанныеКредитовВыданных Кредит ID],
    [ДанныеКредитовВыданных Валюта Кредита ID],
    [ДанныеКредитовВыданных Валюта Кредита],
    [ДанныеКредитовВыданных Дата Выдачи],
    [ДанныеКредитовВыданных Сумма Кредита],
    [ДанныеКредитовВыданных Кредитный Продукт],
    CreditAmountInMDL

)
SELECT
    d.[ДанныеКредитовВыданных Период],
    d.[ДанныеКредитовВыданных Кредит ID],
    d.[ДанныеКредитовВыданных Валюта Кредита ID],
    d.[ДанныеКредитовВыданных Валюта Кредита],
    d.[ДанныеКредитовВыданных Дата Выдачи],
    d.[ДанныеКредитовВыданных Сумма Кредита],
    d.[ДанныеКредитовВыданных Кредитный Продукт],
    -- Convert the original amount to MDL using the most recent exchange rate
    ROUND(d.[ДанныеКредитовВыданных Сумма Кредита] * ISNULL(rate.Rate, 1), 2) AS CreditAmountInMDL
FROM [ATK].[dbo].[РегистрыСведений.ДанныеКредитовВыданных] d
OUTER APPLY 
(
    -- Get the most recent exchange rate for this currency before or on the loan period
    SELECT TOP 1 v.[Валюта Курс] AS Rate
    FROM [ATK].[mis].[Bronze_РегистрыСведений.Валюта] v
    WHERE v.[Валюта Валюта ID] = d.[ДанныеКредитовВыданных Валюта Кредита ID]
      AND v.[Валюта Период] <= d.[ДанныеКредитовВыданных Период]
    ORDER BY v.[Валюта Период] DESC
) rate;
GO
